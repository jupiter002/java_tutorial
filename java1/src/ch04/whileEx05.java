package ch04;

public class whileEx05 {
	public static void main(String[] args) {
		System.out.println("1~100까지의 홀수");
		int a=0;
		while(a<101) {
			System.out.println(a);
			a+=2;
		}
		
		
		System.out.println("------------");
		
		System.out.println("1~100까지의 짝수");
		
		int b=0;
		while(b<101) {
			System.out.println(b);
			b+=2;
		
		}
		
		
		System.out.println("------------");
		int e=0;
		while(e<101) {
			System.out.println(e);
			e++;
		}
		
		
	}
}
