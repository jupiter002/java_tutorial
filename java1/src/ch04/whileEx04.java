package ch04;

public class whileEx04 {
	public static void main(String[] args) {
		int sum=0;
		int i=1;	//초기식
		while(i<6) {//조건식
			sum+=i;
			i++;	//증감식
		}
		System.out.println(sum);
		
		
		int sum_1=0;
		int i_1=1;
		while(i_1<101) {
			sum_1+=i_1;
			i_1++;
		
		}
		System.out.println(sum_1);
		
		
		
		
		
	}
}
